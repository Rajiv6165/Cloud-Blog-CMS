def scan_python_file(filepath):
    found = set()
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Matches patterns like _("something"), _('something'), gettext("something"), gettext_lazy("something")
    # Using python's ast to parse safely is better, but simple regex handles this nicely for standard calls.
    matches = re.findall(r'(?:gettext|gettext_lazy|_)\(\s*["\'](.*?)["\']\s*\)', content)
    for m in matches:
        # Unescape basic string escapes
        found.add(m.replace('\\"', '"').replace("\\'", "'"))
    return found


def scan_template_file(filepath):
    found = set()
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Matches {% trans "something" %} or {% trans 'something' %}
    trans_matches = re.findall(r'\{%\s*trans\s+["\'](.*?)["\']\s*%\}', content)
    for m in trans_matches:
        found.add(m.replace('\\"', '"').replace("\\'", "'"))
        
    # Matches {% blocktrans ... %}text{% endblocktrans %}
    blocktrans_matches = re.finditer(r'\{%\s*blocktrans(?:[^{}%]*?)%\}(.*?)\{%\s*endblocktrans\s*%\}', content, re.DOTALL)
    for match in blocktrans_matches:
        txt = match.group(1).strip()
        # Clean up double spacing/newlines inside blocktrans text
        txt_clean = re.sub(r'\s+', ' ', txt)
        found.add(txt_clean)
        
    return found


def main():
    py_files = [
        "blog/models.py",
        "blog/forms.py",
        "blog/views.py",
        "blog/ai_views.py"
    ]
    
    template_files = [
        "templates/base.html",
        "templates/blog/post_list.html",
        "templates/blog/post_detail.html",
        "templates/blog/post_form.html",
        "templates/blog/post_confirm_delete.html",
        "templates/blog/dashboard.html",
        "templates/blog/profile_edit.html",
        "templates/blog/profile_form.html",
        "templates/blog/tag_detail.html",
        "templates/registration/login.html",
        "templates/registration/register.html"
    ]
    
    all_strings = set()
    
    for pf in py_files:
        if os.path.exists(pf):
            all_strings.update(scan_python_file(pf))
            
    for tf in template_files:
        if os.path.exists(tf):
            all_strings.update(scan_template_file(tf))
            
    print(f"Extracted {len(all_strings)} unique translatable strings.")
    
    os.makedirs("locale", exist_ok=True)
    
    languages = ["hi", "es", "fr", "de"]
    
    for lang in languages:
        lang_dir = f"locale/{lang}/LC_MESSAGES"
        os.makedirs(lang_dir, exist_ok=True)
        po_path = f"{lang_dir}/django.po"
        mo_path = f"{lang_dir}/django.mo"
        
        # Load existing po file if it exists, otherwise create new
        if os.path.exists(po_path):
            po = polib.pofile(po_path)
        else:
            po = polib.POFile()
            po.metadata = {
                'Project-Id-Version': '1.0',
                'Report-Msgid-Bugs-To': 'you@example.com',
                'POT-Creation-Date': '2026-07-13 18:00+0000',
                'PO-Revision-Date': '2026-07-13 18:00+0000',
                'Last-Translator': 'Antigravity <you@example.com>',
                'Language-Team': 'Antigravity Team',
                'Language': lang,
                'MIME-Version': '1.0',
                'Content-Type': 'text/plain; charset=UTF-8',
                'Content-Transfer-Encoding': '8bit',
            }
            
        # Add new strings
        for s in sorted(all_strings):
            if not s.strip():
                continue
            # Check if entry already exists in PO
            existing_entry = po.find(s)
            if existing_entry:
                # If it exists, update it if dictionary has translation and it was empty
                if not existing_entry.msgstr:
                    translation = TRANSLATIONS.get(lang, {}).get(s, "")
                    if translation:
                        existing_entry.msgstr = translation
            else:
                translation = TRANSLATIONS.get(lang, {}).get(s, "")
                entry = polib.POEntry(
                    msgid=s,
                    msgstr=translation
                )
                po.append(entry)
                
        # Save PO file
        po.save(po_path)
        print(f"Saved {po_path} with {len(po)} translations.")
        
        # Compile and save MO file
        po.save_as_mofile(mo_path)
        print(f"Compiled and saved {mo_path}.")


if __name__ == "__main__":
    main()
